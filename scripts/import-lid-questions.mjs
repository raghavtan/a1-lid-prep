// scripts/import-lid-questions.mjs
//
// Imports the official Leben-in-Deutschland question catalogue from the
// leben-in-deutschland-scrapper project and transforms it into the shape
// used by src/data/lidQuestions.js:
//
//     { id, category: 'general' | 'berlin', question, options[], answer, explanation? }
//
// Source data (MIT, content is the public BAMF Fragenkatalog):
//   https://github.com/leben-in-deutschland/leben-in-deutschland-scrapper
//
// The source bank holds 460 questions: 300 nationwide ("num" is numeric) plus
// 10 per Bundesland ("num" prefixed with a 2-letter state code, e.g. "BE-1").
// This app's LiD side is Berlin-focused, so we keep the 300 general questions
// and the 10 Berlin ("BE-") ones, mapping them to category 'general' / 'berlin'.
//
// Notes on the source data quality:
//   - "num" is NOT unique (e.g. seven entries share num "BE-10") and its numeric
//     suffix is unreliable, so we derive stable ids from the unique "id" SHA-256
//     hash instead. Stable ids keep localStorage study progress intact on re-import.
//   - Many entries carry stray newlines / double spaces; we normalise whitespace.
//   - Some question stems repeat (e.g. "Deutschland ist …") but have different
//     options/answers — these are legitimately distinct, so we do NOT de-duplicate.
//
// Usage:
//   node scripts/import-lid-questions.mjs            # fetch live, write data file
//   node scripts/import-lid-questions.mjs --dry-run  # report only, write nothing
//   SRC=/path/to/question.json node scripts/import-lid-questions.mjs   # use a local file
//
// Re-run whenever you want to refresh the bank from upstream.

import { writeFile, readFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = resolve(__dirname, '../src/data/lidQuestions.js');
const SRC_URL =
  'https://raw.githubusercontent.com/leben-in-deutschland/leben-in-deutschland-scrapper/main/data/question.json';

// Image-based questions reference external image URLs and have placeholder
// options ("1".."4"), so they are unanswerable as plain text. Flip this to true
// once the UI can render question images (the `image` URL is carried through).
const INCLUDE_IMAGE_QUESTIONS = false;

const STATE_PREFIX = 'BE'; // Berlin. Change to keep a different Bundesland.
const DRY_RUN = process.argv.includes('--dry-run');

const isGeneral = (num) => /^[0-9]+$/.test(num);
const isBerlin = (num) => num.startsWith(`${STATE_PREFIX}-`);

// Collapse stray newlines / runs of whitespace the scraper leaves in strings.
const clean = (s) => String(s ?? '').replace(/\s+/g, ' ').trim();

async function loadSource() {
  if (process.env.SRC) {
    return JSON.parse(await readFile(process.env.SRC, 'utf8'));
  }
  const res = await fetch(SRC_URL);
  if (!res.ok) throw new Error(`Fetch failed: ${res.status} ${res.statusText}`);
  return res.json();
}

function transform(raw) {
  const skipped = { noSolution: [], image: [], otherState: 0 };
  const out = [];

  for (const q of raw) {
    const num = String(q.num);
    const general = isGeneral(num);
    const berlin = isBerlin(num);

    if (!general && !berlin) { skipped.otherState++; continue; }

    if (!q.solution || !'abcd'.includes(q.solution)) {
      skipped.noSolution.push(num);
      continue;
    }

    const hasImage = q.image && q.image !== '-';
    if (hasImage && !INCLUDE_IMAGE_QUESTIONS) {
      skipped.image.push(num);
      continue;
    }

    const entry = {
      // Stable, unique id derived from the source SHA-256 hash (num is unreliable).
      id: `${general ? 'g' : 'b'}-${q.id.slice(0, 12)}`,
      category: general ? 'general' : 'berlin',
      question: clean(q.question),
      options: [clean(q.a), clean(q.b), clean(q.c), clean(q.d)],
      answer: 'abcd'.indexOf(q.solution),
    };
    const explanation = clean(q.context);
    if (explanation) entry.explanation = explanation;
    if (hasImage) entry.image = q.image;
    out.push(entry);
  }

  // General block first (source order), then Berlin block (source order).
  out.sort((a, b) => (a.category === b.category ? 0 : a.category === 'general' ? -1 : 1));

  return { out, skipped };
}

function render(questions) {
  const header = `// src/data/lidQuestions.js
// GENERATED by scripts/import-lid-questions.mjs — do not edit by hand.
// Source: leben-in-deutschland-scrapper (MIT). Content: public BAMF Fragenkatalog.
// category: "general" | "berlin"  · answer = index into options[]
// The mock-exam logic auto-scales the pass mark to whatever size the bank is.
export const lidQuestions = ${JSON.stringify(questions, null, 2)};
`;
  return header;
}

const raw = await loadSource();
const { out, skipped } = transform(raw);

const general = out.filter((q) => q.category === 'general').length;
const berlin = out.filter((q) => q.category === 'berlin').length;

console.log(`Source questions:        ${raw.length}`);
console.log(`Kept:                    ${out.length}  (general ${general}, berlin ${berlin})`);
console.log(`Skipped — other states:  ${skipped.otherState}`);
console.log(`Skipped — no solution:   ${skipped.noSolution.length}  [${skipped.noSolution.join(', ')}]`);
console.log(
  `Skipped — image-only:    ${skipped.image.length}` +
    (INCLUDE_IMAGE_QUESTIONS ? ' (included)' : '  (set INCLUDE_IMAGE_QUESTIONS=true to keep)'),
);

if (DRY_RUN) {
  console.log('\n--dry-run: no file written.');
} else {
  await writeFile(OUT_PATH, render(out), 'utf8');
  console.log(`\nWrote ${out.length} questions → ${OUT_PATH}`);
}
